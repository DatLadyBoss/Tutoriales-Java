public class tut16 {

	public static void main(String[] args) {
		
		int i;
		
		for (i=0 ; i<10 ; i++){
			//se repite mientras se cumpla la condicion
			System.out.println("Ciclo, i: "+i);
		}
		
		System.out.println("Estoy fuera del ciclo! i: "+i);

	}
}
